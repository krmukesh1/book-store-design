const searchForm = document.querySelector(".search-form");
const searchBtn = document.querySelector("#search-btn");
const loginForm = document.querySelector(".login-form-container");
const loginBtn = document.querySelector("#login-btn");
const closeBtn = document.querySelector("#close-login-btn");
const preLoader = document.querySelector(".loader-container");

// search button
function btnClick() {
  searchForm.classList.toggle("active");
}
searchBtn.addEventListener("click", btnClick);

// loginForm
function loginFormBtn() {
  loginForm.classList.toggle("active");
}
loginBtn.addEventListener("click", loginFormBtn);

// remove login btn
function closeLoginBtn() {
  loginForm.classList.remove("active");
}
closeBtn.addEventListener("click", closeLoginBtn);
// scroll
window.onscroll = () => {
  searchForm.classList.remove("active");
  if (window.scrollY > 80) {
    document.querySelector(".header .header-2").classList.add("active");
  } else {
    document.querySelector(".header .header-2").classList.remove("active");
  }
};
window.onload = () => {
  if (window.scrollY > 80) {
    document.querySelector(".header .header-2").classList.add("active");
  } else {
    document.querySelector(".header .header-2").classList.remove("active");
  }
  fadeOut();
};

// loader
function loader() {
  preLoader.classList.add("active");
}
function fadeOut() {
  setTimeout(loader, 3000);
}

